val1=float(input("Enter the first number:"))
op=input("Enter the operator:")
val2=float(input("Enter the second number:"))
if op=='+':
    print(val1+val2)
elif op=='-':
    print(val1-val2)
elif op=='*':
    print(val1*val2)
elif op=='/':
    print(val1/val2)
else:
    print("invalid")


           


